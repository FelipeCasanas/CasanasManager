    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        //Obtiene el metodo de busqueda y el criterio
        String searchBy = searchDiscriminant.getSelectedItem().toString();
        String search = IOOperations.sanitizeInput(searchParam.getText().toString());

        //Busca la informacion del vehiculo en la base de datos
        Item item = new Item();
        String[] vehicleData = item.searchItem(search);

        //Valida que el array tenga informacion; Si no la tiene imprime mensaje indicando que no se entrontro coincidencia
        if (vehicleData[0] != null) {
            //Quita mensaje de no se encontro coincidencia
            itsNotLabel.setText("-");
            
            //Imprime la informacion obtenida del vehiculo
            vehicleType.setText(vehicleData[9].toUpperCase());  //Pendiente
            vehiclePlate.setText(vehicleData[7].toUpperCase()); //Pendiente
            vehicleOwner.setText(vehicleData[8]);
            vehicleState.setText(vehicleData[2].toUpperCase());
            
            
            //Si vehicleData[5] (QUE ES CHECKOUT_BY) es 0 indica que el vehiculo no ha salido del parqueadero
            if(vehicleData[5].equals("indefinido")) {
                inParking.setText("SI");
            } else {
                inParking.setText("NO");
            }
            
        } else {
            vehicleType.setText("------------");
            vehiclePlate.setText("------------");
            vehicleOwner.setText("------------");
            vehicleState.setText("------------");
            inParking.setText("------------");
            itsNotLabel.setText("NO SE ENCONTRO COINCIDENCIA");
        }
    }//GEN-LAST:event_searchButtonActionPerformed

    private void searchParamFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_searchParamFocusGained
        String placeholder = "Ingrese el dato de busqueda";

        // Eliminar el placeholder al ganar foco
        if (searchParam.getText().equals(placeholder)) {
            searchParam.setText("");
            searchParam.setForeground(Color.GRAY);
        }
    }//GEN-LAST:event_searchParamFocusGained

    private void searchParamFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_searchParamFocusLost
        String placeholder = "Ingrese el dato de busqueda";

        // Restaurar el placeholder si el campo está vacío
        if (searchParam.getText().isEmpty()) {
            searchParam.setText(placeholder);
            searchParam.setForeground(Color.GRAY);
        }
    }//GEN-LAST:event_searchParamFocusLost

